      function  dexp2(arg)
      real*8 arg, dexp2
      if(arg.gt. -138.15511) go to 10
      dexp2 = 0.0d+00
      return
10    if(arg.lt.  140.45   ) go to 20
      dexp2 = 1.0d+60
      return
20    dexp2 = dexp(arg)
      return
      end
