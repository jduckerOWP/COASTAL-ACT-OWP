    real(fp) :: rhow !  Description and declaration in esm_alloc_real.f90
!
!! executable statements -------------------------------------------------------
!
    !'Ag': gravity
    !
    ag = 9.8137
    call prop_get(gdp%mdfile_ptr,'*','Ag',ag)
    !
    !'Rhow': water density
    !
    rhow = 1000.
    call prop_get(gdp%mdfile_ptr,'*','Rhow',rhow)
